package codes;

public class MainDrive {

	public static void main(String[] args) {
		
		for(int i=0; i<5; i++) {
			System.out.println("안녕하세요.");
		}
		System.out.println("========================");
		for(int i=1; i<11; i++) {
			System.out.println(i);
		}
		System.out.println("========================");
		for(int i=20; i>10; i--) {
			System.out.println(i);
		}
		System.out.println("========================");
		for(int i=1; i<=10; i++) {
			System.out.println(i*2);
		}
		
		System.out.println("========================");
		for(int j=1; j<=9; j++) {
			System.out.println("2 x " + j + " = " + 2*j);
		}
//		연습문제 풀이
	}

}
